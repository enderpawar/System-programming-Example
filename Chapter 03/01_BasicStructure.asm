; -------------------------------------------------------
; 디렉티브(Directive)란?
;   - 어셈블러에게 명령을 내리는 키워드 (런타임에 실행되지 않음)
;   - 변수 정의, 세그먼트 선언, 메모리 배치 등을 담당
;   - 대소문자 구분 없음 (.code / .CODE 동일)
; -------------------------------------------------------

.386                        ; 32비트 IA-32 명령어 집합 사용 선언
.MODEL flat, stdcall        ; flat: 단일 평면 메모리 모델, stdcall: Windows 호출 규약
.STACK 4096                 ; 런타임 스택 크기: 4096바이트 (= 메모리 1페이지)

ExitProcess PROTO, dwExitCode:DWORD  ; Windows ExitProcess 서비스 프로토타입

; -------------------------------------------------------
; .DATA 디렉티브: 변수(데이터)를 선언하는 데이터 세그먼트 시작
; -------------------------------------------------------
.DATA
    ; DWORD: 32비트(4바이트) 부호 없는 정수형 변수 선언
    result  DWORD 0     ; 결과값을 저장할 변수, 초기값 0

; -------------------------------------------------------
; .CODE 디렉티브: 실행 가능한 명령어가 위치하는 코드 세그먼트 시작
; -------------------------------------------------------
.CODE

; main PROC: 프로시저 시작. 진입점(entry point)은 보통 main으로 지정
main PROC

    ; -------------------------------------------------------
    ; 명령어(Instruction)란?
    ;   - 어셈블러가 기계어로 번역하여 CPU가 런타임에 실행하는 문장
    ;   - 구성: [레이블:] 니모닉 [피연산자...] [; 주석]
    ; -------------------------------------------------------

    ; mov  : 소스(오른쪽) 값을 목적지(왼쪽)에 복사하는 명령어
    ;        목적지 피연산자의 내용이 변경됨
    mov eax, 10         ; 정수 10을 EAX 레지스터에 저장
    add eax, 20         ; EAX = EAX + 20 = 30

    ; x86은 메모리-메모리 직접 연산 불가 -> 레지스터를 중간 저장소로 사용
    mov result, eax     ; EAX(30)를 메모리 변수 result에 저장

    ; -------------------------------------------------------
    ; NOP (No Operation) 명령어:
    ;   - 아무 동작도 하지 않음, 1바이트를 차지
    ;   - 컴파일러/어셈블러가 코드를 더블워드 경계에 정렬할 때 사용(그래야 4바이트씩 끊어서 빨리 읽음)
    ;   - x86은 짝수 더블워드 주소에서 코드/데이터를 더 빠르게 읽음
    ; -------------------------------------------------------
    nop                 ; 1바이트 점유, 아무 동작 없음

    INVOKE ExitProcess, 0   ; 프로그램 종료, OS에 제어 반환

main ENDP   ; 프로시저 끝 (PROC와 동일한 이름 사용)

; END 디렉티브: 어셈블 대상의 마지막 줄 표시 + 진입점(main) 지정
; 이 줄 이후 내용은 어셈블러가 무시함
END main
