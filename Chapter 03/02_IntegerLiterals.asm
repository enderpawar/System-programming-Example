; ============================================================
; 파일명  : 02_IntegerLiterals.asm
; 설  명  : 정수 리터럴, 상수 표현식, 실수/문자/문자열 리터럴 예제
;           (PDF 3장 - Integer Literals, Constant Integer Expressions,
;                      Real Number Literals, Character/String Literals)
; ============================================================

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

.DATA
; -------------------------------------------------------
; 정수 리터럴(Integer Literal)
;   형식: [부호] 숫자들 [진법문자]
;   진법 문자(radix):
;     h 또는 H  = 16진수 (hexadecimal)
;     b 또는 B  = 2진수  (binary)
;     o 또는 O  = 8진수  (octal)
;     d 또는 D  = 10진수 (decimal, 기본값이므로 생략 가능)
; -------------------------------------------------------

    ; 10진수 리터럴 - 가장 일반적인 형태
    dec1    DWORD 26            ; 십진수 26

    ; 16진수 리터럴
    ;   주의: 문자로 시작하는 경우 앞에 반드시 0을 붙여야 함
    ;   예) A1h (X)  ->  0A1h (O)
    hex1    DWORD 26h           ; 16진수 26h = 십진수 38
    hex2    DWORD 0A3h          ; 문자로 시작하므로 앞에 0 필요

    ; 2진수 리터럴
    bin1    DWORD 1101b         ; 2진수 1101 = 십진수 13
    
    ; 음수 리터럴 (선택적 부호)
    negVal  SDWORD -26          ; 부호 있는 32비트 정수 -26

; -------------------------------------------------------
; 상수 정수 표현식(Constant Integer Expression)
;   - 정수 리터럴과 산술 연산자로 구성된 수식
;   - 어셈블 시간(assembly time)에만 평가됨 (런타임 X)
;   - 결과는 32비트 정수(0 ~ FFFFFFFFh) 범위 내여야 함
;
;   산술 연산자 우선순위 (높음 -> 낮음):
;     () 괄호 > + - (단항) > * / MOD > + - (이항)
; -------------------------------------------------------

    ; 어셈블 시간에 16 * 20 = 320 으로 계산됨
    expr1   DWORD 16 * 20

    ; 어셈블 시간에 (5 + 3) * 2 - 1 = 15 로 계산됨
    expr2   DWORD (5 + 3) * 2 - 1

    ; MOD 연산자: 나머지 계산 (17 MOD 5 = 2)
    expr3   DWORD 17 MOD 5

; -------------------------------------------------------
; 실수 리터럴(Real Number Literal)
;   형식 1) 십진 실수 - 부호(선택) + 정수부 + 소수점 + 분수부(선택) + 지수(선택)
;           최소한 한 자리 숫자와 소수점은 반드시 있어야 함
;   형식 2) 인코딩 실수 - IEEE 부동소수점 형식의 16진수로 표현
; -------------------------------------------------------

    ; REAL4: 4바이트 단정밀도 부동소수점 (IEEE 754 single)
    r4val   REAL4 3.14          ; 원주율 근사값

    ; REAL8: 8바이트 배정밀도 부동소수점 (IEEE 754 double)
    r8val   REAL8 2.71828       ; 자연로그 밑 근사값

    ; REAL10: 10바이트 확장정밀도 부동소수점
    r10val  REAL10 1.0

    ; 지수 표기법
    r4exp   REAL4 3.14E2        ; 3.14 x 10^2 = 314.0

; -------------------------------------------------------
; 문자 리터럴(Character Literal)
;   - 단일 따옴표(' ') 또는 이중 따옴표(" ")로 묶인 한 글자
;   - 메모리에는 해당 문자의 ASCII 코드값(이진수)으로 저장됨 
;   예) 'A' -> 65 (십진수) = 41h (16진수)
; -------------------------------------------------------

    charA   BYTE 'A'            ; 문자 A의 ASCII 코드 41h(65) 저장
    charZ   BYTE "Z"            ; 이중 따옴표도 허용, 90(5Ah) 저장

; -------------------------------------------------------
; 문자열 리터럴(String Literal)
;   - 따옴표로 묶인 문자들의 연속
;   - 공백도 문자열에 포함됨
;   - 메모리에 각 문자의 ASCII 바이트 값으로 순서대로 저장됨
;   예) "ABCD" -> 41h 42h 43h 44h (4바이트)
;
;   널 종료 문자열(null-terminated string):
;   - 가장 널리 쓰이는 문자열 형식
;   - 문자열 끝에 0(널 문자, \0)을 붙임
;   - C언어의 문자열 표현 방식과 동일
; -------------------------------------------------------

    ; 일반 문자열 (4바이트: 41h 42h 43h 44h)
    str1    BYTE "ABCD"

    ; 널 종료 문자열 (5바이트: 41h 42h 43h 44h 00h)
    str2    BYTE "ABCD", 0

    ; CR(0Dh) + LF(0Ah) : 줄바꿈 문자 시퀀스
    ;   CR/LF를 표준 출력에 쓰면 커서가 다음 줄 맨 앞으로 이동
    str3    BYTE "Hello", 0Dh, 0Ah, 0    ; "Hello" + 줄바꿈 + 널

    ; 줄 연속 문자(\): 소스 코드 두 줄을 하나의 문장으로 합침
    ;   반드시 줄의 마지막 문자여야 함
    str4    BYTE "This is a long string that ",\
                 "spans two lines", 0

.CODE
main PROC
    ; 이 예제는 데이터 정의 방법을 보여주기 위한 것으로
    ; 별도의 연산 없이 종료
    INVOKE ExitProcess, 0
main ENDP

END main
