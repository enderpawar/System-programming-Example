; ──────────────────────────────────────────────────────────
; [문제 1] = (등호) 디렉티브
;
;   COUNT = 10
;   COUNT = (1)______     ; = 는 재정의가 (2) 가능 / 불가능?
;   mov ecx, COUNT        ; 어셈블 시 → mov ecx, (3)______
; ──────────────────────────────────────────────────────────

COUNT = 10
COUNT = 20

; ──────────────────────────────────────────────────────────
; [문제 2] EQU 디렉티브 — 세 가지 형식
;
;   형식 (1): 이름 EQU 정수표현식
;   형식 (2): 이름 EQU 기존심볼이름
;   형식 (3): 이름 EQU <임의 텍스트>
;
;   ROWS  EQU  5
;   COLS  EQU  10
;   AREA  EQU  (4)______
;
;   PI    EQU  (5)______   ; 3.14159 를 텍스트로 저장
;
;   ROWS  EQU  999         ; 이 줄은 (6) 올바름 / 오류?
;                          ; 이유: (7)______
; ──────────────────────────────────────────────────────────

ROWS  EQU  5
COLS  EQU  10
AREA  EQU  ROWS * COLS

; ──────────────────────────────────────────────────────────
; [문제 3] TEXTEQU 디렉티브
;
;   greeting  TEXTEQU  (8)______
;   copyMov   TEXTEQU  (9)______
;   cnt       TEXTEQU  %(10 + 5)    ; cnt = (10)______
;
;   greeting  TEXTEQU  <"Hi",0>     ; TEXTEQU 재정의는 (11) 가능 / 불가능?
; ──────────────────────────────────────────────────────────

; ──────────────────────────────────────────────────────────
; [문제 4] =, EQU, TEXTEQU 비교 — 빈칸을 채우세요.
;
;   디렉티브    값의 종류       재정의 가능?
;   =           (12)______      (13) 가능 / 불가능
;   EQU         (14)______      (15) 가능 / 불가능
;   TEXTEQU     (16)______      (17) 가능 / 불가능
; ──────────────────────────────────────────────────────────

; ──────────────────────────────────────────────────────────
; [문제 5] 다음 코드에서 어셈블 오류가 발생하는 줄은?
;
;   (줄A)  MAX   = 100
;   (줄B)  MAX   = 200
;   (줄C)  LIMIT EQU 50
;   (줄D)  LIMIT EQU 100
;   (줄E)  MSG   TEXTEQU <"Hi">
;   (줄F)  MSG   TEXTEQU <"Bye">
;
;   오류 발생 줄: (18)______ ; 줄D
;   이유: (19)______ ; EQU는 정의된 심볼을 재정의할 수 없다.
; ──────────────────────────────────────────────────────────

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

.DATA

    ; [문제 6] $ 연산자로 배열 크기 계산 — 빈칸을 채우세요.

    arr   BYTE   10, 20, 30, 40, 50
    ArrSize  = ( (20)______ - arr )
    ; ArrSize = (21)______

    dArr  DWORD  1, 2, 3, 4
    ; dArr의 원소 개수 = ($ - dArr) / (22)______  4
    ; = (23)______ ;4 

.CODE
main PROC
    ; [문제 7] 다음 코드에서 ecx에 들어가는 값은?
    ;   COUNT = 10
    ;   mov ecx, COUNT    ; ecx = (24)______
    ;   COUNT = 30
    ;   mov ecx, COUNT    ; ecx = (25)______

    mov ecx, COUNT

    INVOKE ExitProcess, 0
main ENDP
END main
