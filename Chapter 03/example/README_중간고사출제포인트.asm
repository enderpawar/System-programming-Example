; 1. 리틀 엔디안 (Little-Endian)
;    DWORD 12345678h 를 주소 200h 에 저장 →
;    200h: 78h  201h: 56h  202h: 34h  203h: 12h  (LSB 먼저)
;
; 2. = vs EQU vs TEXTEQU 재정의 가능 여부
;    =        : 정수 표현식만 / 재정의 가능 O
;    EQU      : 정수 또는 텍스트 / 재정의 불가 X  ← 시험 단골
;    TEXTEQU  : 텍스트만 / 재정의 가능 O
;
; 3. $ 연산자 (현재 위치 카운터)
;    list  BYTE  10,20,30,40,50
;    ListSize = ($ - list)         → 5 (BYTE 5개)
;    wList WORD  1,2,3,4
;    wSize = ($ - wList) / 2       → 4 (WORD이므로 /2)
;
; 4. 16진수 리터럴 규칙
;    A-F 로 시작하면 앞에 0 필수: FFh → 오류!  0FFh → 올바름
;
; 5. DUP 연산자
;    BYTE 10 DUP(0)    → 0 이 10개
;    BYTE 5  DUP(?)    → 미초기화 5바이트
;
; 6. .DATA? — 실행 파일 크기 절약 (파일에 데이터 포함 안 됨)
;    DUP(?) 와 함께 사용
;
; 7. 디렉티브 vs 명령어
;    디렉티브: BYTE WORD DWORD .DATA .CODE PROC ENDP END (어셈블 시간)
;    명령어  : mov add sub nop jmp loop    (런타임)
