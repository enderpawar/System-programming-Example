; ============================================================
; 파일명  : 03_DataTypes.asm
; 설  명  : 고유 데이터 타입(Intrinsic Data Types) 정의 예제
;           (PDF 3장 - Defining Data: BYTE/WORD/DWORD/QWORD,
;                      Little-Endian Order, Uninitialized Data)
; ============================================================
;
; 데이터 정의 문(Data Definition Statement) 기본 형식:
;   [이름]  타입지시어  초기값[, 초기값, ...]
;
;   - 이름    : 선택 사항. 변수의 메모리 오프셋을 가리키는 레이블
;   - 타입지시어: BYTE, WORD, DWORD 등 크기를 지정
;   - 초기값  : 최소 하나 필요. 미초기화 시 ? 사용
;   - 모든 초기값은 어셈블러에 의해 이진 데이터로 변환됨
; ============================================================

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

; -------------------------------------------------------
; 초기화된 데이터 세그먼트
; -------------------------------------------------------
.DATA

    ; ==================================================
    ; BYTE / SBYTE (1바이트 = 8비트)
    ;   BYTE  : 0 ~ 255 (부호 없는 정수)
    ;   SBYTE : -128 ~ 127 (부호 있는 정수)
    ;   레거시 디렉티브: DB (Data Byte)
    ; ==================================================
    value1  BYTE  10        ; 부호 없는 8비트 정수, 값 = 10
    value2  BYTE  0FFh      ; 최댓값 = 255
    sval    SBYTE -128      ; 부호 있는 8비트 최솟값

    ; 여러 초기값: 첫 번째 이름만 레이블이 됨
    ;   list[0]=10, list[1]=20, list[2]=30 처럼 동작
    list    BYTE  10, 20, 30, 40

    ; 레이블 없이 이전 배열을 이어서 선언 가능
            BYTE  50, 60    ; list 배열의 5번째, 6번째 요소

    ; 다양한 진법 혼합 초기화 (같은 BYTE 배열 안에서 가능)
    mixed   BYTE  0Ah, 11, 0001_0000b   ; 16진, 10진, 2진 혼합

    ; ==================================================
    ; WORD / SWORD (2바이트 = 16비트)
    ;   WORD  : 0 ~ 65535
    ;   SWORD : -32768 ~ 32767
    ;   레거시 디렉티브: DW (Define Word)
    ; ==================================================
    wval1   WORD  1000h     ; 부호 없는 16비트
    swval   SWORD -300      ; 부호 있는 16비트

    ; 16비트 워드 배열
    wArray  WORD  1, 2, 3, 4, 5

    ; ==================================================
    ; DWORD / SDWORD (4바이트 = 32비트)
    ;   DWORD  : 0 ~ 4294967295 (0 ~ 0FFFFFFFFh)
    ;   SDWORD : -2147483648 ~ 2147483647
    ;   레거시 디렉티브: DD (Define Doubleword)
    ; ==================================================
    dval1   DWORD  12345678h
    sdval   SDWORD -100000

    ; 포인터 변수: 다른 변수의 오프셋(주소)을 저장
    ;   val3의 주소를 pVal에 저장
    val3    DWORD  0ABCD1234h
    pVal    DWORD  val3            ; pVal = val3의 메모리 오프셋

    ; 32비트 더블워드 배열
    dArray  DWORD  100h, 200h, 300h

    ; ==================================================
    ; QWORD (8바이트 = 64비트)
    ;   레거시 디렉티브: DQ (Define Quadword)
    ; ==================================================
    qval    QWORD  1234567890ABCDEFh    ; 64비트 정수

    ; ==================================================
    ; 실수형 (Floating-Point Types)
    ;   REAL4  : 4바이트 단정밀도  (= DD로도 정의 가능)
    ;   REAL8  : 8바이트 배정밀도  (= DQ로도 정의 가능)
    ;   REAL10 : 10바이트 확장정밀도 (= DT로도 정의 가능)
    ; ==================================================
    r4      REAL4  3.14
    r8      REAL8  3.141592653589793 
    r10     REAL10 3.14159265358979323846

    ; ==================================================
    ; ? 초기화: 미초기화(uninitialized) 변수 선언
    ;   변수는 존재하지만 초기값이 정의되지 않음
    ;   런타임 시 해당 메모리의 값은 예측 불가
    ; ==================================================
    undef1  BYTE  ?         ; 미초기화 1바이트
    undef2  DWORD ?         ; 미초기화 4바이트

    ; ==================================================
    ; 리틀 엔디안 순서 (Little-Endian Order)
    ;   x86 프로세서는 다중 바이트 데이터를 메모리에 저장할 때 
    ;   낮은 바이트(LSB)를 낮은 주소에 먼저 저장
    ;
    ;   예) DWORD 12345678h를 주소 100h부터 저장하면:
    ;     주소 100h: 78h  (최하위 바이트)
    ;     주소 101h: 56h
    ;     주소 102h: 34h
    ;     주소 103h: 12h  (최상위 바이트)
    ;
    ;   빅 엔디안(Big-Endian)은 반대 순서 (최상위 바이트가 먼저)
    ; ==================================================
    leDemo  DWORD  12345678h    ; 메모리에는 78 56 34 12 순서로 저장됨 -> 이거 기출이였음 매우매우매우 중요 리

; -------------------------------------------------------
; 미초기화 데이터 세그먼트 (.DATA?)
;   - 초기화하지 않을 대용량 데이터 선언에 사용
;   - .DATA?를 사용하면 컴파일된 실행 파일 크기가 줄어듦
;     (.DATA에 0으로 초기화하면 파일에 0 데이터가 그대로 포함됨)
; -------------------------------------------------------
.DATA?
    bigBuf  BYTE 10000 DUP(?)   ; 10000바이트 미초기화 버퍼
                                 ; 실행 파일 크기를 늘리지 않음

; -------------------------------------------------------
; 코드와 데이터 혼합 (Mixing Code and Data)
;   어셈블러는 .CODE와 .DATA 사이를 자유롭게 전환 가능
; -------------------------------------------------------
.CODE
main PROC

    ; DWORD 변수에 값을 읽어 EAX에 저장
    mov eax, dval1      ; eax = 12345678h

    ; 다시 .DATA 세그먼트를 열어 데이터 정의 가능
    .DATA
        tempVal DWORD 0 ; 코드 중간에 데이터 삽입 (어셈블러가 허용)

    .CODE
    mov tempVal, eax    ; 코드로 돌아와서 계속 실행

    INVOKE ExitProcess, 0
main ENDP

END main
