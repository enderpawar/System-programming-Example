; ║              SECTION 3: 데이터 정의                      ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 3-1. 내장 데이터 타입 (Intrinsic Data Types) 전체 표
; ─────────────────────────────────────────────────────────
;
;   ┌──────────┬──────┬──────────────────────────────────────┐
;   │ 타입     │ 크기 │ 범위                                 │
;   ├──────────┼──────┼──────────────────────────────────────┤
;   │ BYTE     │ 1B   │ 0 ~ 255                              │
;   │ SBYTE    │ 1B   │ -128 ~ 127                           │
;   │ WORD     │ 2B   │ 0 ~ 65,535                           │
;   │ SWORD    │ 2B   │ -32,768 ~ 32,767                     │
;   │ DWORD    │ 4B   │ 0 ~ 4,294,967,295                    │
;   │ SDWORD   │ 4B   │ -2,147,483,648 ~ 2,147,483,647       │
;   │ QWORD    │ 8B   │ 0 ~ 18,446,744,073,709,551,615       │
;   │ REAL4    │ 4B   │ IEEE 754 단정밀도 (≈ 7자리 유효숫자) │
;   │ REAL8    │ 8B   │ IEEE 754 배정밀도 (≈ 15자리 유효숫자)│
;   │ REAL10   │ 10B  │ IEEE 754 확장정밀도                  │
;   └──────────┴──────┴──────────────────────────────────────┘
;
;   레거시 디렉티브 (구형, 호환용):
;     DB = BYTE,  DW = WORD,  DD = DWORD/REAL4
;     DQ = QWORD/REAL8,  DT = REAL10
;
; ─────────────────────────────────────────────────────────
; 3-2. 데이터 정의 문 (Data Definition Statement)
; ─────────────────────────────────────────────────────────
;   형식: [이름]  타입지시어  초기값[, 초기값, ...]
;
;   · 이름(선택): 변수의 메모리 오프셋을 가리키는 레이블
;               여러 초기값을 줄 경우 첫 번째 오프셋만 가리킴
;   · 초기값: 최소 하나 필요. 미초기화는 ? 사용
;   · 초기값이 여러 개 → 배열처럼 연속 메모리 할당
;
;   예)
;     val1 BYTE 10h          ; 1바이트, 0x10
;     list BYTE 10, 20, 30   ; 3바이트 배열
;          BYTE 40, 50       ; 레이블 없이 이어서 정의 가능
;     undef DWORD ?          ; 4바이트 미초기화
;
; ─────────────────────────────────────────────────────────
; 3-3. DUP 연산자
; ─────────────────────────────────────────────────────────
;   형식: 횟수  DUP (초기값[, 초기값, ...])
;   용도: 동일한 값을 여러 번 반복 할당 (배열/버퍼 초기화)
;
;   예)
;     BYTE 10 DUP(0)      → 0이 10개: {0,0,0,...}
;     BYTE 5  DUP(?)      → 미초기화 5바이트
;     WORD 8  DUP(0)      → 0인 워드 8개 = 16바이트
;     BYTE 2  DUP(3 DUP(0FFh))  → 중첩 DUP: {FF,FF,FF,FF,FF,FF}
;       (2 × 3 = 6바이트)
;
; ─────────────────────────────────────────────────────────
; 3-4. 리틀 엔디안 순서 (Little-Endian Order) ★ 시험 단골 ★
; ─────────────────────────────────────────────────────────
;   · x86 프로세서는 다중 바이트 데이터를 저장할 때
;     최하위 바이트(LSB)를 가장 낮은 주소에 먼저 저장
;
;   예) DWORD 12345678h 를 주소 0100h 부터 저장:
;     주소 0100h: 78h   ← 최하위 바이트 (LSB) 먼저
;     주소 0101h: 56h
;     주소 0102h: 34h
;     주소 0103h: 12h   ← 최상위 바이트 (MSB) 나중
;
;   빅 엔디안(Big-Endian): 반대 순서 — 최상위 바이트가 낮은 주소
;   (네트워크 프로토콜, 일부 RISC CPU에서 사용)
;
;   ☑ 퀴즈 3: DWORD 0ABCD1234h 가 주소 200h에 저장될 때
;             주소 200h, 201h, 202h, 203h 각각의 값은?
;     → 200h: 34h  201h: 12h  202h: CDh  203h: ABh
;
; ─────────────────────────────────────────────────────────
; 3-5. .DATA? 디렉티브 (미초기화 데이터)
; ─────────────────────────────────────────────────────────
;   · 초기화하지 않을 대용량 데이터를 선언하는 별도 세그먼트
;   · 실행 파일 크기를 줄여줌
;     → .DATA에 0 초기화하면 그 0들이 파일에 그대로 포함
;     → .DATA?는 런타임에 OS가 할당, 파일에는 크기 정보만 기록
;
;   예) .DATA?
;         bigBuf BYTE 10000 DUP(?)  ; 실행파일에 10000바이트 추가 안 됨
;
; ─────────────────────────────────────────────────────────
; 3-6. 코드와 데이터 혼합 (Mixing Code and Data)
; ─────────────────────────────────────────────────────────
;   · 어셈블러는 .CODE와 .DATA 사이를 자유롭게 전환 허용
;   · 코드 중간에 .DATA 섹션을 열어 변수를 추가할 수 있음
;   예)
;     .CODE
;     main PROC
;       .DATA
;         tempVal DWORD 0   ; 코드 중간에 데이터 정의
;       .CODE
;       mov tempVal, eax    ; 바로 사용 가능
;
;   ☑ 퀴즈 4: 다음 중 실행 파일 크기를 가장 적게 늘리는 선언은?
;     a) .DATA    bigBuf BYTE 10000 DUP(0)
;     b) .DATA?   bigBuf BYTE 10000 DUP(?)
;     → 답: b)  (.DATA?는 파일에 0을 포함시키지 않음)

; ╔══════════════════════════════════════════════════════════╗
; ║            SECTION 4: 심볼릭 상수                        ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 4-0. 심볼릭 상수란?
; ─────────────────────────────────────────────────────────
;   · 식별자(심볼)에 정수 표현식이나 텍스트를 연결한 것
;   · 메모리를 예약하지 않음 (변수와 다름!)
;   · 어셈블러 전처리 단계에서만 사용 → 런타임에 변경 불가
;
; ─────────────────────────────────────────────────────────
; 4-1. = (등호) 디렉티브
; ─────────────────────────────────────────────────────────
;   형식: 이름 = 정수표현식
;   · 이름을 정수 표현식과 연결
;   · 어셈블 시간에 모든 이름이 숫자로 대체됨
;   · ★ 같은 파일에서 재정의(재할당) 가능
;
;   예)
;     COUNT = 10          ; ECX를 10으로 설정하는 심볼
;     LIMIT = 100
;     LIMIT = 200         ; 재정의 가능 (합법적)
;     mov ecx, COUNT      ; 어셈블 시 → mov ecx, 10
;
; ─────────────────────────────────────────────────────────
; 4-2. $ 연산자 (현재 위치 카운터, Current Location Counter)
; ─────────────────────────────────────────────────────────
;   · $ = 현재 프로그램 문장의 오프셋 반환
;   · 배열/문자열의 크기를 어셈블러가 자동으로 계산할 때 활용
;   · ★ $ 심볼과 배열 레이블의 차이 = 배열이 차지하는 바이트 수
;
;   예)
;     list      BYTE 10, 20, 30, 40, 50
;     ListSize  = ($ - list)         ; = 5 (바이트 5개)
;
;     wList     WORD 100, 200, 300, 400
;     wListSize = ($ - wList) / 2    ; = 4 (WORD = 2바이트이므로 /2)
;
;     dList     DWORD 1000, 2000, 3000
;     dListSize = ($ - dList) / 4    ; = 3 (DWORD = 4바이트이므로 /4)
;
;   ☑ 퀴즈 5: 다음 코드에서 ArrSize의 값은?
;     arr      WORD 10, 20, 30, 40, 50
;     ArrSize  = ($ - arr) / 2
;     → ArrSize = 5
;
; ─────────────────────────────────────────────────────────
; 4-3. EQU 디렉티브
; ─────────────────────────────────────────────────────────
;   세 가지 형식:
;     (1) 이름 EQU 정수표현식     → = 과 유사
;     (2) 이름 EQU 기존심볼이름   → 별칭(alias) 생성
;     (3) 이름 EQU <임의 텍스트>  → 정수가 아닌 텍스트도 가능
;
;   · ★ = 와 달리 같은 소스 파일에서 재정의 불가
;     → 실수로 같은 심볼에 다른 값을 할당하는 오류 방지
;
;   예)
;     ROWS EQU 5
;     COLS EQU 10
;     SIZE EQU ROWS * COLS           ; (1) 정수: 50
;     PI   EQU <3.14159>             ; (3) 텍스트: 정수 아님
;     PressKey EQU <"Press any key", 0>  ; (3) 텍스트
;     ; ROWS EQU 999  ← 오류! EQU는 재정의 불가
;
; ─────────────────────────────────────────────────────────
; 4-4. TEXTEQU 디렉티브 (텍스트 매크로)
; ─────────────────────────────────────────────────────────
;   세 가지 형식:
;     (1) 이름 TEXTEQU <텍스트>         → 텍스트 직접 할당
;     (2) 이름 TEXTEQU 기존텍스트매크로  → 기존 매크로 복사
;     (3) 이름 TEXTEQU %(정수표현식)     → 정수를 텍스트로 변환
;
;   · ★ EQU와 달리 재정의 가능
;   · 텍스트 매크로끼리 중첩·조합 가능
;
;   예)
;     moveVal TEXTEQU <mov eax>        ; "mov eax" 텍스트
;     count5  TEXTEQU %(5)             ; "5" 텍스트
;     myStr   TEXTEQU <"Hello", 0>
;     myStr   TEXTEQU <"Updated", 0>   ; 재정의 가능
;
; ─────────────────────────────────────────────────────────
; 4-5. =, EQU, TEXTEQU 비교 표 (시험 단골!)
; ─────────────────────────────────────────────────────────
;
;   ┌────────────┬────────────────┬──────────┬────────────┐
;   │ 디렉티브   │ 값의 종류      │ 재정의   │ 사용 예    │
;   ├────────────┼────────────────┼──────────┼────────────┤
;   │ =          │ 정수 표현식만  │ 가능 ✓   │ COUNT = 10 │
;   ├────────────┼────────────────┼──────────┼────────────┤
;   │ EQU        │ 정수 또는 텍스트│ 불가 ✗  │ PI EQU <3.14>│
;   ├────────────┼────────────────┼──────────┼────────────┤
;   │ TEXTEQU    │ 텍스트만       │ 가능 ✓   │ A TEXTEQU <mov>│
;   └────────────┴────────────────┴──────────┴────────────┘
;
;   ☑ 퀴즈 6: 아래 코드에서 오류가 발생하는 줄은?
;     COUNT   = 10
;     COUNT   = 20        ; (A)
;     SIZE    EQU 50
;     SIZE    EQU 100     ; (B)
;     MSG     TEXTEQU <"Hi">
;     MSG     TEXTEQU <"Bye">  ; (C)
;     → 오류: (B)  —  EQU는 재정의 불가

; ╔══════════════════════════════════════════════════════════╗
; ║              SECTION 5: 흔한 실수 모음                   ║
; ╚══════════════════════════════════════════════════════════╝
;
; ✗ 실수 1: 16진수 리터럴이 문자로 시작할 때 0 누락
;   hex1 BYTE A3h         ; 오류! A로 시작하면 식별자로 해석됨
;   → hex1 BYTE 0A3h      ; 올바름: 앞에 0 추가
;
; ✗ 실수 2: DWORD 범위를 초과하는 즉치값을 BYTE에 저장
;   val BYTE 300          ; 오류! 300 > 255 (BYTE 최댓값)
;   → val BYTE 0FFh       ; 올바름: 0~255 범위 유지
;
; ✗ 실수 3: 메모리-메모리 직접 MOV
;   mov var2, var1        ; 오류! 둘 다 메모리 피연산자
;   → mov al, var1 / mov var2, al   ; 레지스터 경유
;
; ✗ 실수 4: 리틀 엔디안 순서 혼동
;   DWORD 12345678h → 메모리에 78 56 34 12 순서 (낮은 주소부터 LSB)
;   12 34 56 78 순서로 저장된다고 착각하지 말 것!
;
; ✗ 실수 5: 코드 레이블에 콜론(:) 누락
;   target  mov eax, 0   ; 오류! target이 변수로 해석될 수 있음
;   → target: mov eax, 0 ; 올바름: 코드 레이블엔 반드시 :
;
; ✗ 실수 6: EQU로 정의한 심볼 재정의 시도
;   MAX EQU 100
;   MAX EQU 200          ; 오류! EQU는 재정의 불가
;   → = 를 사용하거나, TEXTEQU로 교체
;
; ✗ 실수 7: END 디렉티브 이후에 코드 작성
;   END main
;   mov eax, 1           ; 어셈블러가 완전히 무시 (실행 안 됨)
;
; ✗ 실수 8: .DATA?에서 초기화된 값 선언
;   .DATA?
;   buf BYTE 10 DUP(0)   ; 기술적으로 어셈블은 됨
;                        ; 하지만 .DATA?의 장점(파일 크기 절약) 없음
;   → .DATA?에는 DUP(?)만 사용하는 것이 의도에 맞음

; ╔══════════════════════════════════════════════════════════╗
; ║           SECTION 6: 전체 퀴즈 정답 모음                 ║
; ╚══════════════════════════════════════════════════════════╝
;
; Q1: 디렉티브와 명령어 구분 → DWORD가 디렉티브
;
; Q2: 링커의 역할
;     → obj + lib → exe, CALL 목적지 주소 채움
;
; Q3: DWORD 0ABCD1234h @ 주소 200h 의 리틀 엔디안 배치
;     → 200h: 34h  201h: 12h  202h: CDh  203h: ABh
;
; Q4: 파일 크기를 줄이는 선언 → .DATA? + DUP(?)
;
; Q5: arr WORD 5개 → ($ - arr) / 2 = 5
;
; Q6: EQU 재정의 → (B) SIZE EQU 100 에서 오류
;
; ─────────────────────────────────────────────────────────
; [3·4·5장 연계 핵심 암기 사항]
; ─────────────────────────────────────────────────────────
;   3장: 리틀 엔디안, = vs EQU vs TEXTEQU 재정의 여부
;   4장: MOV 크기 일치 규칙, CF vs OF, MOVZX vs MOVSX
;   5장: CALL/RET 스택 동작, USES의 EAX 함정, Irvine32 I/O
