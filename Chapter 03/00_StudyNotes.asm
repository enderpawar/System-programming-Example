; ─────────────────────────────────────────────────────────
; 1-1. 정수 리터럴 (Integer Literals)
; ─────────────────────────────────────────────────────────
;   형식: [부호] 숫자들 [진법문자]
;
;   ┌──────┬──────┬───────────────────────────────────────┐
;   │ 진법  │ 접미사│ 예                                   │
;   ├──────┼──────┼───────────────────────────────────────┤
;   │ 10진수│ d / 없음│ 26,  26d                          │
;   │ 16진수│ h    │ 1Ah, 0FFh  ← 문자 시작 시 앞에 0 필수│
;   │ 2진수 │ b    │ 1101b                                 │
;   │ 8진수 │ o    │ 36o                                   │
;   └──────┴──────┴───────────────────────────────────────┘
;
;   ★ 핵심 규칙: 16진수가 A~F로 시작할 경우 앞에 반드시 0 추가
;     A3h  → 오류!   0A3h → 올바름
;     FFh  → 오류!   0FFh → 올바름
;
; ─────────────────────────────────────────────────────────
; 1-2. 상수 정수 표현식 (Constant Integer Expressions)
; ─────────────────────────────────────────────────────────
;   · 정수 리터럴과 산술 연산자로 구성된 수식
;   · 어셈블 시간(assembly time)에만 평가됨 — 런타임 X
;   · 결과는 32비트 정수 범위(0 ~ FFFFFFFFh) 내여야 함
;
;   연산자 우선순위 (높음 → 낮음):
;     () > 단항 + - > * / MOD > 이항 + -
;
;   예) 16 * 20       → 320 (어셈블 시간에 계산)
;       (5+3)*2 - 1   → 15
;       17 MOD 5      → 2  (나머지 연산)
;
; ─────────────────────────────────────────────────────────
; 1-3. 리터럴 종류 요약
; ─────────────────────────────────────────────────────────
;   실수 리터럴 (Real Number Literal)
;     형식 A (십진 실수): [부호] 정수부 . [소수부] [E 지수]
;       최소 한 자리 숫자와 소수점 필수
;       예) 3.14,  -1.5E2  (= -150.0),  1.  (= 1.0)
;     형식 B (인코딩): IEEE 부동소수점을 16진수로 표현
;
;   문자 리터럴 (Character Literal)
;     · 단따옴표(' ') 또는 쌍따옴표(" ")로 감싼 한 글자
;     · 메모리에 ASCII 코드값으로 저장
;     · 'A' → 41h (65),  '0' → 30h (48)
;
;   문자열 리터럴 (String Literal)
;     · 따옴표로 감싼 문자들의 연속
;     · "ABCD" → 41h 42h 43h 44h (4바이트)
;     · 가장 흔한 형태: 널 종료 문자열 (끝에 0 추가)
;       "Hello", 0  →  48 65 6C 6C 6F 00 (6바이트)
;     · CR(0Dh) + LF(0Ah): 줄바꿈 문자 시퀀스
;     · \ (줄 연속 문자): 소스 두 줄을 하나의 정의로 합침
;
; ─────────────────────────────────────────────────────────
; 1-4. 식별자 (Identifiers)
; ─────────────────────────────────────────────────────────
;   · 변수, 상수, 프로시저, 코드 레이블에 붙이는 이름
;   · 1 ~ 247 글자
;   · 대소문자 구분 없음 (myVar == MYVAR)
;   · 첫 글자: 영문자(A-Z), _(언더스코어), @, ?, $
;   · 이후 글자: 위에 숫자(0-9) 추가 가능
;   · 어셈블러 예약어와 동일한 이름 불가
;
; ─────────────────────────────────────────────────────────
; 1-5. 디렉티브 vs 명령어 (Directive vs Instruction)
; ─────────────────────────────────────────────────────────
;   디렉티브(Directive)
;     · 어셈블러가 어셈블 시간에 처리
;     · 런타임에 실행되지 않음
;     · 변수 정의, 세그먼트 선언, 메모리 배치 등
;     · 대소문자 구분 없음
;     · 예) .DATA, .CODE, DWORD, BYTE, PROC, ENDP, END
;
;   명령어(Instruction)
;     · 어셈블러가 기계어로 번역 → CPU가 런타임에 실행
;     · 구성: [레이블:] 니모닉 [피연산자...] [; 주석]
;     · 예) mov eax, 5  /  add eax, ebx  /  nop
;
;   ☑ 퀴즈 1: 다음 중 디렉티브는? (명령어와 구분)
;     a) mov  b) DWORD  c) add  d) nop
;     → 답: b) DWORD
;
; ─────────────────────────────────────────────────────────
; 1-6. 명령어 구성 요소 4가지
; ─────────────────────────────────────────────────────────
;   [레이블:] 니모닉 [피연산자] [; 주석]
;
;   · 레이블(Label, 선택): 명령어나 데이터의 주소 표시자
;       코드 레이블: 이름 뒤에 반드시 콜론(:) 붙임
;         예) target:  mov eax, 0
;       데이터 레이블: 콜론 없음
;         예) count DWORD 0
;
;   · 니모닉(Mnemonic, 필수): 명령어 이름
;       예) mov, add, sub, jmp
;
;   · 피연산자(Operand, 보통 필수): 0~3개
;       두 개 이상: 첫 번째 = 목적지, 두 번째 = 소스
;       예) mov eax, 5  (목적지=eax, 소스=5)
;
;   · 주석(Comment, 선택): 세미콜론(;) 이후 텍스트, 어셈블러 무시
;
; ─────────────────────────────────────────────────────────
; 1-7. NOP 명령어
; ─────────────────────────────────────────────────────────
;   · No Operation — 아무 동작도 하지 않음
;   · 기계어 1바이트 차지 (opcode: 90h)
;   · 용도: 코드를 더블워드(4바이트) 경계에 정렬
;     → x86은 짝수 더블워드 주소에서 코드/데이터를 더 빠르게 처리

