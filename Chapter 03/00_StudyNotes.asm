; ║         SECTION 2: 프로그램 구조와 어셈블 과정           ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 2-1. 표준 프로그램 템플릿 (암기 필수!)
; ─────────────────────────────────────────────────────────
;
;   .386                            ← 32비트 IA-32 명령어 집합 선언
;   .MODEL flat, stdcall            ← 메모리 모델 / 호출 규약
;   .STACK 4096                     ← 스택 크기 (4096 = 메모리 1페이지)
;
;   ExitProcess PROTO, dwExitCode:DWORD  ← Windows 종료 서비스 프로토타입
;
;   .DATA                           ← 데이터 세그먼트 시작
;       (변수 선언)
;
;   .CODE                           ← 코드 세그먼트 시작
;   main PROC
;       (실행 명령어)
;       INVOKE ExitProcess, 0       ← 프로그램 종료
;   main ENDP                       ← 프로시저 종료 (PROC와 동일 이름)
;
;   END main                        ← 어셈블 대상 끝 + 진입점 지정
;
; ─────────────────────────────────────────────────────────
; 2-2. 핵심 디렉티브 설명
; ─────────────────────────────────────────────────────────
;   .386
;     · 32비트 IA-32(x86) 명령어 집합 사용 선언
;
;   .MODEL flat, stdcall
;     · flat: 단일 평면 메모리 모델 (32비트 프로그램에서 항상 사용)
;     · stdcall: Windows 서비스 호출에 필요한 호출 규약
;
;   .STACK 4096
;     · 런타임 스택 크기 예약 (4096바이트 = 1 메모리 페이지)
;     · 서브루틴 호출, 인수 전달, 지역 변수에 사용
;
;   .DATA  /  .CODE
;     · 세그먼트 선언: .DATA = 변수 영역, .CODE = 명령어 영역
;     · 코드 중간에 .DATA / .CODE를 반복해 섞을 수 있음
;
;   PROC / ENDP
;     · 프로시저(서브루틴)의 시작과 끝을 표시
;     · PROC와 ENDP에 같은 이름을 써야 함
;
;   END 레이블
;     · 어셈블러가 처리할 마지막 줄 표시
;     · 뒤에 진입점(entry point) 레이블 지정
;     · END 이후의 코드는 어셈블러가 무시
;
; ─────────────────────────────────────────────────────────
; 2-3. 어셈블-링크-실행 사이클 (Assemble-Link-Execute Cycle)
; ─────────────────────────────────────────────────────────
;
;   [Step 1] 소스 파일 작성 (.asm)
;       텍스트 에디터로 어셈블리 소스 작성
;
;   [Step 2] 어셈블러 (Assembler)
;       소스 파일 → 오브젝트 파일 (.obj)
;       기계어로 번역; 오류 발생 시 Step 1으로 복귀
;       선택적으로 리스팅 파일(.lst) 생성
;
;   [Step 3] 링커 (Linker)
;       오브젝트 파일 + 라이브러리 (.lib) → 실행 파일 (.exe)
;       CALL된 라이브러리 프로시저를 복사해 주소를 채움
;
;   [Step 4] OS 로더 (Loader)
;       실행 파일을 메모리에 적재 → CPU가 진입점부터 실행
;
; ─────────────────────────────────────────────────────────
; 2-4. 리스팅 파일 (.lst)
; ─────────────────────────────────────────────────────────
;   · 어셈블러가 생성하는 디버그용 파일
;   · 내용: 소스 코드 + 줄 번호 + 각 명령어의 주소 + 기계어 바이트 + 심볼 테이블
;   · 예) 줄 11: 00000000  B8 00000005  → mov eax, 5
;         opcode B8 = EAX에 즉치 복사, 00000005 = 즉치값 5
;
;   ☑ 퀴즈 2: 어셈블-링크-실행 사이클에서 링커의 역할은?
;     → 오브젝트 파일(.obj)과 라이브러리(.lib)를 결합해
;       실행 파일(.exe)을 생성하고, CALL 목적지 주소를 채움

