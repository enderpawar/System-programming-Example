; ============================================================
; [시험 대비 빈칸 채우기] 4장 - MOV, MOVZX, MOVSX, XCHG
; 빈칸( ______ )을 채우세요. 정답은 E4_01_MOV_Answer.asm 참고.
; ============================================================

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

.DATA
    var1  BYTE  10h
    var2  BYTE  20h
    xval  WORD  100
    yval  WORD  200

.CODE
main PROC

; ──────────────────────────────────────────────────────────
; [문제 1] MOV 규칙 — 다음 조합이 가능(O) 또는 불가(X) 인지 표시하세요.
;
;   a) MOV reg, imm    : (1)______
;   b) MOV reg, reg    : (2)______
;   c) MOV mem, mem    : (3)______
;   d) MOV mem, reg    : (4)______
;   e) MOV mem, imm    : (5)______
;   f) MOV eax, bl     : (6)______
; ──────────────────────────────────────────────────────────

; [문제 2] 다음 코드 중 오류인 줄을 찾고 수정하세요.
;
;   (줄A)  mov var2, var1      ; (7) 올바름 / 오류 — 이유: ______
;   (줄B)  mov eax, bl        ; (8) 올바름 / 오류 — 이유: ______
;   (줄C)  mov al, 0FFh       ; (9) 올바름 / 오류
;   (줄D)  mov eax, 0         ; (10) 올바름 / 오류
;
;   (줄A) 의 올바른 수정:
;       mov (11)______, var1
;       mov var2, (12)______
;
;   (줄B) 의 올바른 수정:
;       (13)______ eax, bl
;       (14)______ eax, bl

; ──────────────────────────────────────────────────────────
; [문제 3] 겹치는 레지스터 (Overlapping Registers)
;
;   EAX = 12345678h 상태에서:
;   mov AL, 0AAh 실행 후 EAX = (15)______
;   상위 3바이트는 (16) 변함 / 그대로 유지
;
;   EAX의 구조:
;     EAX  = 32비트 전체
;     AX   = EAX의 (17)______ 16비트
;     AH   = AX의  (18)______ 8비트
;     AL   = AX의  (19)______ 8비트
; ──────────────────────────────────────────────────────────

; [문제 4] MOVZX vs MOVSX — 실행 결과를 채우세요.
;
;   BL = 0A0h
;
;   movzx eax, bl  →  EAX = (20)______   (21)______ 확장
;   movsx eax, bl  →  EAX = (22)______   (23)______ 확장
;
;   BL = 0F0h 일 때:
;   movzx eax, bl  →  EAX = (24)______   10진수 = (25)______
;   movsx eax, bl  →  EAX = (26)______   10진수 = (27)______

; ──────────────────────────────────────────────────────────
; [문제 5] XCHG — 두 메모리 변수를 교환하는 코드를 완성하세요.
;
;   xval = 100, yval = 200 교환:
;
;   mov  ax, xval
;   (28)______ ax, yval
;   mov  xval, (29)______
;
;   교환 후: xval = (30)______, yval = (31)______

; ──────────────────────────────────────────────────────────
; [문제 6] 직접 오프셋 피연산자 (Direct-Offset Operands)
;
;   arrayB  BYTE  10h, 20h, 30h, 40h, 50h
;
;   mov al, arrayB          ; AL = (32)______
;   mov al, [arrayB + 1]    ; AL = (33)______
;   mov al, [arrayB + 4]    ; AL = (34)______
;
;   arrayW  WORD  100h, 200h, 300h 에서 두 번째 원소 접근:
;   mov ax, [arrayW + (35)______]

    INVOKE ExitProcess, 0
main ENDP
END main
