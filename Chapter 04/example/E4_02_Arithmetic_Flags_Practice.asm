; ============================================================
; [시험 대비 빈칸 채우기] 4장 - 산술 명령어 & CPU 플래그
; 빈칸( ______ )을 채우세요. 정답은 E4_02_Arithmetic_Flags_Answer.asm 참고.
; ============================================================

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

.DATA
    Xval  SDWORD  -10
    Yval  SDWORD   30
    Zval  SDWORD    5
    rval  SDWORD    0

.CODE
main PROC

; ──────────────────────────────────────────────────────────
; [문제 1] CPU 플래그 — 각 플래그가 1이 되는 조건을 채우세요.
;
;   플래그   이름
;   CF       Carry Flag      → (1)______ //부호없는 정수에서 오버플로 발생 
;   OF       Overflow Flag   → (2)______ //부호 있는 정수 오버플로 / 언더플로 발생
;   ZF       Zero Flag       → (3)______
;   SF       Sign Flag       → (4)______
;   PF       Parity Flag     → (5)______
; ──────────────────────────────────────────────────────────

; [문제 2] INC / DEC 와 플래그
;
;   INC 와 DEC 는 (6)______ 플래그에 영향을 주지 않는다. //CF 
;   이유: (7)______ // 루프에서 INC/DEC 연산을 사용할 떄가 많은데, 이때 CF 값을 보전하기 위해서.

; ──────────────────────────────────────────────────────────
; [문제 3] 플래그 예측 — 실행 후 각 플래그 값을 채우세요.
;
;   [예제 A]  AL = 127 (= 7Fh)
;   add al, 1
;               AL = (8)______ // 128
;               CF = (9)______   이유: (10)______ ; 0, 부호 없는 정수의 관점에서 범위초과가 존재하지 않는다.
;               OF = (11)______  이유: (12)______ ; 1, 부호 있는 정수 관점에서 양수간 덧셈으로 인해 음수로 변했다. 
;               ZF = (13)______ 0 
;               SF = (14)______ 1
;
;   [예제 B]  AL = 0FFh
;   add al, 1
;               AL = (15)______ ; 0
;               CF = (16)______  이유: (17)______ ; 1, 부호있는 정수의 관점에서 carry가 발생했다 
;               OF = (18)______  이유: (19)______ ; 0 부호있는 관점에서 음수와 양수가 더해져 0이되었으므로 오버플로가 없다.
;               ZF = (20)______ 0 
; ──────────────────────────────────────────────────────────

; [문제 4] NEG 명령어
;
;   · NEG 동작: 모든 비트 반전 후 (21)______ 더하기 -> 기본적으로 NEG는 0-Val 연산을 수행한다! 
;   · 0이 아닌 값에 NEG → CF = (22)______ 
;   · 0에 NEG           → CF = (23)______, ZF = (24)______ 
;   · SBYTE -128에 NEG  → 결과 = (25)______, OF = (26)______

; ──────────────────────────────────────────────────────────
; [문제 5] 고수준 표현식을 어셈블리로 변환
;
;   C++: rval = -Xval + (Yval - Zval)
;
;   mov eax, (27)______ ; Xval 
;   (28)______ eax ;NEG 
;   mov ebx, (29)______ ; e
;   (30)______ ebx, Zval mov 
;   (31)______ eax, ebx sub 
;   mov (32)______, eax rval 
;
;   Xval=-10, Yval=30, Zval=5 일 때 rval = (33)______

; ──────────────────────────────────────────────────────────
; [문제 6] OFFSET 연산자 — 빈칸을 채우세요.
;
;   mov esi, (34)______ myArray ;OFFSET 
;
;   OFFSET 연산자의 반환 크기: (35)______ ; 32비트 ? 무조건 4바이트인가..주소는 무조건 4바이트인가보다   
;
;   포인터 변수 선언:
;   pArr  DWORD  (36)______ bigArray ; OFFSET 

; ──────────────────────────────────────────────────────────
; [문제 7] TYPE / LENGTHOF / SIZEOF — 빈칸을 채우세요.
;
;   arr  DWORD  1, 2, 3, 4, 5
;
;   TYPE     arr = (37)______ 4
;   LENGTHOF arr = (38)______ 5 
;   SIZEOF   arr = (39)______ 20 
;
;   공식: SIZEOF = (40)______ × (41)______ 

    mov eax, Xval
    neg eax
    mov ebx, Yval
    sub ebx, Zval
    add eax, ebx
    mov rval, eax

    INVOKE ExitProcess, 0
main ENDP
END main
