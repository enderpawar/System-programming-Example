; ============================================================
; 파일명  : 06_MOV_DataTransfer.asm
; 설  명  : 데이터 전송 명령어 예제
;           MOV / MOVZX / MOVSX / LAHF / SAHF / XCHG /
;           직접 오프셋 피연산자(Direct-Offset Operands)
;           (PDF 4장 - Data Transfer Instructions)
; ============================================================
;
; [피연산자(Operand) 세 가지 유형]
;   1) 즉치(Immediate) : 숫자 리터럴  예) 5, 0FFh
;   2) 레지스터(Register): CPU 레지스터  예) eax, bl, cx
;   3) 메모리(Memory)  : 메모리 주소 참조  예) [var1], myArray
; ============================================================

.386
.MODEL flat, stdcall
.STACK 4096

ExitProcess PROTO, dwExitCode:DWORD

.DATA
    ; 직접 메모리 피연산자 예제용 변수들
    var1    BYTE  10h           ; 8비트 변수
    var2    WORD  2000h         ; 16비트 변수
    var3    DWORD 12345678h     ; 32비트 변수

    ; 부호 있는 변수 (MOVSX 예제용)
    sval8   SBYTE -16           ; 8비트 부호 있는 정수 -16
    sval16  SWORD -200          ; 16비트 부호 있는 정수

    ; 배열 (직접 오프셋 예제용)
    arrayB  BYTE  10h, 20h, 30h, 40h, 50h   ; 5바이트 배열
    arrayW  WORD  100h, 200h, 300h           ; 3워드(6바이트) 배열
    arrayD  DWORD 1000h, 2000h, 3000h        ; 3더블워드(12바이트) 배열

    ; XCHG 예제용 변수
    xval    WORD  1234h
    yval    WORD  5678h

.CODE
main PROC

    ; ==========================================================
    ; MOV 명령어 - 소스에서 목적지로 데이터 복사
    ;   형식: MOV 목적지, 소스
    ;   규칙:
    ;     1) 두 피연산자의 크기가 같아야 함
    ;     2) 두 피연산자가 모두 메모리일 수 없음
    ;     3) IP/EIP/RIP 레지스터는 목적지가 될 수 없음
    ;   C++의 대입(=)과 같이 오른쪽에서 왼쪽으로 데이터가 이동
    ; ==========================================================

    ; [즉치 -> 레지스터]
    mov eax, 5              ; EAX = 5

    ; [레지스터 -> 레지스터]
    mov ebx, eax            ; EBX = EAX = 5

    ; [메모리 -> 레지스터] : var1(10h)을 AL에 복사
    mov al, var1            ; AL = 10h
    ; 대괄호를 써도 동일 - 명시적 역참조 표현 (권장)
    mov al, [var1]          ; AL = 10h (위와 동일)

    ; [레지스터 -> 메모리]
    mov var1, al            ; var1 = AL

    ; [메모리 -> 메모리] 직접 이동은 불가! 레지스터를 거쳐야 함
    ; mov var3, var1        <- 오류!
    mov al, var1
    mov var1, al            ; 올바른 방법: 레지스터를 중간 매개로 사용

    ; ----------------------------------------------------------
    ; 겹치는 레지스터 (Overlapping Values)
    ;   EAX(32bit) > AX(16bit) > AH(8bit, 상위) / AL(8bit, 하위)
    ;   작은 단위에 값을 쓰면 나머지 부분은 변경되지 않음
    ; ----------------------------------------------------------
    mov eax, 0              ; EAX = 00000000h
    mov ax,  0B8A7h         ; EAX = 0000B8A7h  (하위 16비트만 변경)
    mov al,  0CDh           ; EAX = 0000B8CDh  (하위 8비트만 변경)
    ; AX에 0을 쓰면 EAX의 하위 16비트가 0이 됨 (상위 16비트 유지)
    mov ax, 0               ; EAX = 00000000h

    ; ==========================================================
    ; MOVZX - 제로 확장 이동 (Zero-Extend)
    ;   작은 부호 없는(unsigned) 정수를 더 큰 레지스터로 복사할 때
    ;   빈 상위 비트를 0으로 채움
    ;   형식: MOVZX 큰레지스터, 작은소스
    ;   소스는 상수(즉치)가 될 수 없음
    ; ==========================================================

    ; 8비트 -> 16비트 제로 확장
    movzx ax, var1          ; var1=10h -> AX = 0010h (상위 8비트=0)

    ; 8비트 -> 32비트 제로 확장
    movzx eax, var1         ; var1=10h -> EAX = 00000010h

    ; 16비트 -> 32비트 제로 확장
    movzx eax, var2         ; var2=2000h -> EAX = 00002000h

    ; 레지스터 -> 레지스터 (8비트 BL -> 16비트 AX)
    mov bl, 0A5h
    movzx ax, bl            ; AX = 00A5h

    ; 주의: 부호 있는 값에 MOVZX를 사용하면 잘못된 결과!
    ; sval8 = -16 = 0F0h (8비트 표현)
    movzx ecx, sval8        ; ECX = 000000F0h (= +240, -16이 아님!)
    ; 부호 있는 값에는 MOVSX를 사용해야 함

    ; ==========================================================
    ; MOVSX - 부호 확장 이동 (Sign-Extend)
    ;   작은 부호 있는(signed) 정수를 더 큰 레지스터로 복사할 때
    ;   소스의 최상위 비트(부호 비트)로 빈 상위 비트를 채움
    ;     부호 비트=0(양수) -> 상위 비트 모두 0
    ;     부호 비트=1(음수) -> 상위 비트 모두 1
    ; ==========================================================

    ; 8비트 부호 있는 값 -16 = 0F0h -> 32비트로 확장
    movsx eax, sval8        ; EAX = FFFFFFF0h (= -16 올바르게 유지)

    ; 16비트 부호 있는 값 -> 32비트 확장

    
    movsx eax, sval16       ; 부호 비트로 상위 16비트 채움

    ; 레지스터 -> 레지스터 부호 확장
    mov bl, 0F0h            ; BL = F0h (최상위 비트=1, 음수)
    movsx ax, bl            ; AX = FFF0h (부호 확장으로 상위 8비트=FFh)

    ; ==========================================================
    ; LAHF / SAHF 명령어
    ;   LAHF (Load AH from Flags): EFLAGS의 하위 바이트 -> AH 복사
    ;     복사되는 플래그: 부호(SF), 영(ZF), 보조캐리(AF), 패리티(PF), 캐리(CF)
    ;   SAHF (Store AH into Flags): AH -> EFLAGS 하위 바이트 복사
    ;   활용: 플래그 상태를 메모리에 백업/복원할 때 사용
    ; ==========================================================

    lahf                    ; AH = 현재 EFLAGS 하위 8비트
    ; 여기서 다른 연산을 해도 이전 플래그는 AH에 보존됨
    sahf                    ; AH의 값을 다시 EFLAGS에 복원

    ; ==========================================================
    ; XCHG 명령어 - 두 피연산자의 값을 교환
    ;   형식: XCHG op1, op2
    ;   규칙: MOV와 동일하지만, 즉치 피연산자는 사용 불가
    ; ==========================================================

    ; 레지스터 <-> 레지스터 교환
    mov  eax, 1111h
    mov  ebx, 2222h
    xchg eax, ebx           ; EAX=2222h, EBX=1111h (값이 서로 바뀜)

    ; 레지스터 <-> 메모리 교환
    xchg ax, xval           ; AX <-> xval 교환

    ; 두 메모리 변수 교환: XCHG는 메모리-메모리 직접 교환 불가
    ; MOV와 XCHG를 조합하여 해결
    mov  ax, xval           ; AX = xval 임시 저장
    xchg ax, yval           ; AX <-> yval 교환
    mov  xval, ax           ; 원래 yval 값을 xval에 저장

    ; ==========================================================
    ; 직접 오프셋 피연산자 (Direct-Offset Operands)
    ;   변수 이름에 상수 변위(displacement)를 더해 배열 원소에 접근
    ;   표현식이 가리키는 메모리 주소를 유효 주소(effective address)라 함
    ;   대괄호 [ ]는 필수는 아니지만 명시성을 위해 권장
    ;
    ;   주의: MASM은 배열 범위를 검사하지 않음!
    ;         범위를 벗어나면 논리 버그 발생 (조심할 것)
    ; ==========================================================

    ; 바이트 배열: arrayB = {10h, 20h, 30h, 40h, 50h}
    ;   원소 간격: 1바이트
    mov al, arrayB          ; AL = 10h (첫 번째 원소, 오프셋 +0)
    mov al, [arrayB + 1]    ; AL = 20h (두 번째 원소, 오프셋 +1)
    mov al, [arrayB + 2]    ; AL = 30h (세 번째 원소, 오프셋 +2)
    mov al, [arrayB + 4]    ; AL = 50h (다섯 번째 원소, 오프셋 +4)

    ; 워드 배열: arrayW = {100h, 200h, 300h}
    ;   원소 간격: 2바이트 (WORD = 2바이트)
    mov ax, arrayW          ; AX = 100h (첫 번째 원소)
    mov ax, [arrayW + 2]    ; AX = 200h (두 번째 원소, +2바이트)
    mov ax, [arrayW + 4]    ; AX = 300h (세 번째 원소, +4바이트)

    ; 더블워드 배열: arrayD = {1000h, 2000h, 3000h}
    ;   원소 간격: 4바이트 (DWORD = 4바이트)
    mov eax, arrayD         ; EAX = 1000h (첫 번째 원소)
    mov eax, [arrayD + 4]   ; EAX = 2000h (두 번째 원소, +4바이트)
    mov eax, [arrayD + 8]   ; EAX = 3000h (세 번째 원소, +8바이트)

    INVOKE ExitProcess, 0
main ENDP

END main
