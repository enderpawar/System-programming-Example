; ╔══════════════════════════════════════════════════════════╗
; ║          SECTION 1: 데이터 전송 명령어                   ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 1-1. 피연산자 세 가지 유형
; ─────────────────────────────────────────────────────────
;   · 즉치(Immediate) : 숫자 리터럴     예) 5, 0FFh, 'A'
;   · 레지스터(Register): CPU 내부 레지스터  예) eax, bl, cx
;   · 메모리(Memory)  : 데이터 세그먼트 주소  예) myVar, [esi]
;
; ─────────────────────────────────────────────────────────
; 1-2. MOV 명령어 규칙 (시험 단골!)
; ─────────────────────────────────────────────────────────
;   형식: MOV 목적지, 소스
;   ① 양쪽 피연산자의 크기가 반드시 같아야 함
;   ② 양쪽 모두 메모리 피연산자가 될 수 없음 (메모리→메모리 불가)
;   ③ EIP/IP는 목적지가 될 수 없음
;   ④ 즉치값을 세그먼트 레지스터(DS 등)에 직접 MOV 불가
;
;   ┌──────────────────────────────────────┬────────┐
;   │ 조합                                 │ 가능?  │
;   ├──────────────────────────────────────┼────────┤
;   │ MOV reg, imm    (즉치 → 레지스터)   │  ✓     │
;   │ MOV reg, reg    (레지스터 → 레지스터)│  ✓     │
;   │ MOV reg, mem    (메모리 → 레지스터)  │  ✓     │
;   │ MOV mem, reg    (레지스터 → 메모리)  │  ✓     │
;   │ MOV mem, imm    (즉치 → 메모리)      │  ✓     │
;   │ MOV mem, mem    (메모리 → 메모리)    │  ✗     │
;   │ MOV reg(다른크기), reg               │  ✗     │
;   └──────────────────────────────────────┴────────┘
;
; ─────────────────────────────────────────────────────────
; 1-3. 겹치는 레지스터 (Overlapping Registers)
; ─────────────────────────────────────────────────────────
;   EAX (32비트)
;   └─ AX (하위 16비트)
;      ├─ AH (상위 8비트)
;      └─ AL (하위 8비트)
;
;   작은 서브레지스터에 값을 써도 나머지 비트는 그대로 유지됨
;   예) EAX = 12345678h 상태에서 mov AL, 0AAh → EAX = 123456AAh
;
; ─────────────────────────────────────────────────────────
; 1-4. MOVZX vs MOVSX (크기 불일치 해결, 시험 단골!)
; ─────────────────────────────────────────────────────────
;   MOV는 크기가 다른 피연산자 간에 사용 불가
;   → 작은 → 큰 복사 시 MOVZX 또는 MOVSX 사용
;
;   MOVZX (Zero-Extend): 빈 상위 비트를 0으로 채움
;     → 부호 없는(unsigned) 정수에 사용
;     예) BL = 0A0h (1010_0000b)
;         movzx eax, bl → EAX = 000000A0h  (+160, 올바름)
;
;   MOVSX (Sign-Extend): 빈 상위 비트를 소스의 MSB(부호 비트)로 채움
;     → 부호 있는(signed) 정수에 사용
;     예) BL = 0A0h = -96 (부호 있는 해석)
;         movsx eax, bl → EAX = FFFFFFA0h  (-96, 올바름)
;         (MSB=1이므로 상위 비트 모두 1로 채움)
;
;   ☑ 퀴즈 1: BL = F0h일 때 각 명령 후 EAX 값은?
;     movzx eax, bl → EAX = ?   (답: 000000F0h = 240)
;     movsx eax, bl → EAX = ?   (답: FFFFFFF0h = -16)
;
; ─────────────────────────────────────────────────────────
; 1-5. LAHF / SAHF
; ─────────────────────────────────────────────────────────
;   LAHF : EFLAGS 하위 바이트 (SF, ZF, AF, PF, CF) → AH 복사
;   SAHF : AH → EFLAGS 하위 바이트 복사
;   활용 : 플래그 상태를 변수에 저장하거나 나중에 복원할 때
;
; ─────────────────────────────────────────────────────────
; 1-6. XCHG (Exchange)
; ─────────────────────────────────────────────────────────
;   두 피연산자의 값을 교환 (임시 변수 없이 swap)
;   · 즉치(Immediate) 피연산자 사용 불가
;   · 메모리-메모리 직접 교환 불가 → 레지스터를 중간에 사용
;   예) 두 메모리 변수 교환:
;     mov  ax, xval
;     xchg ax, yval 
;     mov  xval, ax
;
; ─────────────────────────────────────────────────────────
; 1-7. 직접 오프셋 피연산자 (Direct-Offset Operands)
; ─────────────────────────────────────────────────────────
;   변수명 + 상수 → 배열 원소에 직접 접근
;   예) arrayB = {10h, 20h, 30h, 40h, 50h}
;     mov al, arrayB        ; AL = 10h (인덱스 0)
;     mov al, [arrayB + 1]  ; AL = 20h (인덱스 1)
;     mov al, [arrayB + 4]  ; AL = 50h (인덱스 4)
;   · 배열 타입별 원소 간격: BYTE=1, WORD=2, DWORD=4
;   · MASM은 배열 범위를 검사하지 않으므로 범위 초과 주의!

; ╔══════════════════════════════════════════════════════════╗
; ║          SECTION 2: 덧셈과 뺄셈, CPU 플래그             ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 2-1. INC / DEC
; ─────────────────────────────────────────────────────────
;   INC : 피연산자 += 1
;   DEC : 피연산자 -= 1
;   영향 플래그: OF, SF, ZF, AF, PF
;   ★ CF(Carry)는 영향을 받지 않음! (시험 자주 출제)
;
; ─────────────────────────────────────────────────────────
; 2-2. ADD / SUB
; ─────────────────────────────────────────────────────────
;   ADD 목적지, 소스  → 목적지 = 목적지 + 소스
;   SUB 목적지, 소스  → 목적지 = 목적지 - 소스
;   피연산자 규칙은 MOV와 동일
;   영향 플래그: CF, OF, SF, ZF, AF, PF
;
; ─────────────────────────────────────────────────────────
; 2-3. NEG (Negate, 부호 반전)
; ─────────────────────────────────────────────────────────
;   피연산자를 2의 보수로 변환 (부호 반전)
;   2의 보수: 모든 비트 반전 후 1 더하기
;   ★ 0이 아닌 값에 NEG → CF = 1 (항상)
;   ★ 0에 NEG → CF = 0, ZF = 1
;   ★ SBYTE 최솟값 -128에 NEG → 결과 -128 (OF = 1, 오버플로!)
;
; ─────────────────────────────────────────────────────────
; 2-4. CPU 플래그 총정리 (시험 핵심!)
; ─────────────────────────────────────────────────────────
;
;   ┌─────┬───────────────┬────────────────────────────────┐
;   │ 기호 │ 이름          │ 1이 되는 조건                  │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ CF  │ Carry Flag    │ 부호 없는 정수 오버플로/언더플로│
;   │     │               │ (MSB에서 올림/빌림 발생)        │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ OF  │ Overflow Flag │ 부호 있는 정수 오버플로/언더플로│
;   │     │               │ 양+양=음, 음+음=양 이 되면 발생 │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ ZF  │ Zero Flag     │ 연산 결과가 정확히 0            │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ SF  │ Sign Flag     │ 결과의 MSB = 1 (음수)           │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ PF  │ Parity Flag   │ 결과 하위 바이트의 1비트 수가   │
;   │     │               │ 짝수 개                         │
;   ├─────┼───────────────┼────────────────────────────────┤
;   │ AF  │ Auxiliary     │ 비트 3에서 비트 4로 올림 발생   │
;   │     │ Carry Flag    │ (BCD 연산에서 주로 사용)        │
;   └─────┴───────────────┴────────────────────────────────┘
;
;   CF vs OF 구분 핵심:
;     CF → 부호 없는(unsigned) 연산의 오류 감지에 사용
;     OF → 부호 있는(signed)  연산의 오류 감지에 사용
;     CPU는 부호 있는지 없는지 모름 → 프로그래머가 어떤 플래그를
;     볼지 결정해야 함
;
;   INC/DEC의 CF 무영향 이유:
;     연속된 INC/DEC가 CF를 건드리지 않도록 설계되어
;     이전 연산의 CF를 보존하며 루프 등에서 사용 가능
;
; ─────────────────────────────────────────────────────────
; 2-5. 산술 표현식 구현 (고수준 언어 → 어셈블리 변환)
; ─────────────────────────────────────────────────────────
;   C++: rval = -Xval + (Yval - Zval)
;   어셈블리 변환:
;     mov eax, Xval     ; 항1: -Xval 계산
;     neg eax           ; EAX = -Xval
;     mov ebx, Yval     ; 항2: (Yval - Zval) 계산
;     sub ebx, Zval     ; EBX = Yval - Zval
;     add eax, ebx      ; EAX = -Xval + (Yval - Zval)
;     mov rval, eax
;
;   ☑ 퀴즈 2: AL = 127 (7Fh)일 때 add al, 1 후 플래그는?
;     → AL = 80h = -128, OF = 1 (부호 있는 오버플로!)
;        CF = 0 (부호 없는 관점에선 127+1=128, 정상)
;
;   ☑ 퀴즈 3: AL = 0FFh일 때 add al, 1 후 플래그는?
;     → AL = 0, CF = 1 (부호 없는 오버플로!)
;        ZF = 1 (결과 = 0)
;        OF = 0 (부호 있는 관점: -1+1=0, 정상)

; ╔══════════════════════════════════════════════════════════╗
; ║       SECTION 3: 데이터 관련 연산자와 디렉티브           ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 3-1. OFFSET 연산자
; ─────────────────────────────────────────────────────────
;   변수의 데이터 세그먼트 시작 기준 오프셋(주소)을 반환
;   반환값: DWORD (32비트 모드)
;   예) mov esi, OFFSET myArray   ; ESI = myArray의 주소
;   포인터 초기화: pArray DWORD OFFSET bigArray
;
; ─────────────────────────────────────────────────────────
; 3-2. ALIGN 디렉티브
; ─────────────────────────────────────────────────────────
;   다음 변수를 특정 바이트 경계에 정렬
;   경계값: 1(기본), 2, 4, 8, 16
;   필요 시 어셈블러가 빈 패딩 바이트를 자동 삽입
;   이유: CPU는 정렬된(even-address) 데이터를 더 빠르게 처리
;   예) ALIGN 4  → 다음 변수를 4의 배수 주소에 배치
;
; ─────────────────────────────────────────────────────────
; 3-3. PTR 연산자
; ─────────────────────────────────────────────────────────
;   선언된 크기를 재정의(override)하여 다른 크기로 메모리 접근
;   형식: 크기 PTR 피연산자
;   x86은 리틀 엔디안 → 낮은 주소에 하위(low) 바이트 저장
;
;   예) myDouble DWORD 12345678h
;       메모리: [addr+0]=78h, [+1]=56h, [+2]=34h, [+3]=12h
;       mov ax, WORD PTR myDouble       → AX = 5678h (하위 워드)
;       mov al, BYTE PTR myDouble       → AL = 78h   (최하위 바이트)
;       mov al, BYTE PTR [myDouble + 3] → AL = 12h   (최상위 바이트)
;
; ─────────────────────────────────────────────────────────
; 3-4. TYPE / LENGTHOF / SIZEOF 연산자 (암기!)
; ─────────────────────────────────────────────────────────
;   TYPE     : 변수 한 원소의 크기 (바이트)
;   LENGTHOF : 배열의 원소 개수
;   SIZEOF   : 배열 전체 바이트 수 = LENGTHOF × TYPE
;
;   ┌──────────────────────────────────────────────────────┐
;   │ 선언                       TYPE  LENGTHOF  SIZEOF   │
;   ├──────────────────────────────────────────────────────┤
;   │ arr BYTE  10,20,30         1     3         3        │
;   │ arr WORD  10,20,30         2     3         6        │
;   │ arr DWORD 10,20,30         4     3         12       │
;   └──────────────────────────────────────────────────────┘
;
;   ★ 주의: 배열이 여러 줄로 선언될 때
;     myArr WORD 1,2,3    ← LENGTHOF = 3 (레이블과 같은 줄만 셈)
;           WORD 4,5,6
;   LENGTHOF myArr = 3 (첫 줄만), SIZEOF myArr = 12 (전체 바이트)
;
;   ☑ 퀴즈 4: 다음 배열에서 SIZEOF와 LENGTHOF는?
;     arr DWORD 1, 2, 3, 4, 5
;     → SIZEOF = 20, LENGTHOF = 5, TYPE = 4
;
; ─────────────────────────────────────────────────────────
; 3-5. LABEL 디렉티브
; ─────────────────────────────────────────────────────────
;   스토리지를 새로 할당하지 않고 레이블+크기 속성만 삽입
;   같은 메모리 위치에 다른 이름/크기로 접근하는 별칭 생성
;   예)
;     val16  LABEL WORD      ; 스토리지 미할당, WORD 별칭
;     val32  DWORD 0ABCD1234h
;     → val16 접근 시 AX = 1234h (하위 워드)
;     → val32 접근 시 EAX = 0ABCD1234h
;
; ─────────────────────────────────────────────────────────
; 3-6. TYPEDEF 연산자
; ─────────────────────────────────────────────────────────
;   사용자 정의 포인터 타입 생성
;   형식: 이름 TYPEDEF PTR 기존타입
;   데이터 세그먼트(.DATA) 앞에 선언
;   예)
;     PBYTE  TYPEDEF PTR BYTE   ; 바이트 포인터 타입
;     PDWORD TYPEDEF PTR DWORD  ; 더블워드 포인터 타입
;     ptr1   PBYTE  OFFSET myArr  ; PBYTE 타입 포인터 변수

; ╔══════════════════════════════════════════════════════════╗
; ║            SECTION 4: 간접 주소지정                      ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 4-1. 직접 vs 간접 주소지정 비교
; ─────────────────────────────────────────────────────────
;   직접(Direct)   : mov al, myVar      (주소가 상수로 고정)
;   간접(Indirect) : mov esi, OFFSET myVar
;                    mov al, [esi]       (레지스터가 주소를 담음)
;
;   간접 주소지정이 필요한 이유: 배열 원소를 반복 접근할 때
;   레지스터 값을 증가시켜 다음 원소로 이동 가능
;
; ─────────────────────────────────────────────────────────
; 4-2. 간접 피연산자
; ─────────────────────────────────────────────────────────
;   32비트 범용 레지스터를 [ ]로 감싸서 사용
;   [레지스터] = 그 레지스터가 담고 있는 주소의 메모리 내용
;
;   소스로 사용:     mov al, [esi]     ; AL = [ESI]의 값
;   목적지로 사용:   mov [esi], bl     ; [ESI] 위치에 BL 저장
;
;   크기가 불명확한 경우 PTR 필요:
;     inc [esi]           ; ✗ 오류: 크기 불명확
;     inc DWORD PTR [esi] ; ✓ 올바름
;
; ─────────────────────────────────────────────────────────
; 4-3. 배열 순회 시 ESI 증가량
; ─────────────────────────────────────────────────────────
;   BYTE  배열 → add esi, 1  (또는 inc esi)
;   WORD  배열 → add esi, 2
;   DWORD 배열 → add esi, 4
;   (TYPE 연산자로 일반화: add esi, TYPE 배열이름)
;
; ─────────────────────────────────────────────────────────
; 4-4. 인덱스 피연산자 (Indexed Operands) — 두 가지 표기법
; ─────────────────────────────────────────────────────────
;   형식 A: [배열이름 + 레지스터]  ← 가장 자주 사용
;     mov esi, 0
;     mov al, [byteArr + esi]   ; byteArr[esi]
;     inc esi
;     mov al, [byteArr + esi]   ; byteArr[esi+1]
;
;   형식 B: [베이스레지스터 + 상수]
;     mov esi, OFFSET byteArr
;     mov al, [esi + 2]         ; byteArr[2]
;
;   둘 다 유효 주소(effective address) = 배열 오프셋 + 인덱스
;
; ─────────────────────────────────────────────────────────
; 4-5. 스케일 팩터 (Scale Factors)
; ─────────────────────────────────────────────────────────
;   [배열 + 인덱스 * 스케일] 형태로 바이트 오프셋 자동 계산
;   스케일: WORD=2, DWORD=4, QWORD=8
;
;   예) dwordArr에서 인덱스 2번 원소 접근:
;     mov esi, 2
;     mov eax, [dwordArr + esi * 4]  ; dwordArr[2], 바이트 오프셋 = 8
;
;   ☑ 퀴즈 5: wordArr에서 인덱스 3번 원소를 스케일 팩터로 접근하는 코드?
;     mov esi, 3
;     mov ax, [wordArr + esi * 2]    ; wordArr[3]
;
; ─────────────────────────────────────────────────────────
; 4-6. 포인터 (Pointers)
; ─────────────────────────────────────────────────────────
;   다른 변수의 주소를 담는 DWORD 변수 (32비트 near pointer)
;   데이터 선언: ptrB DWORD OFFSET arrayB
;   사용 방법:
;     mov esi, ptrB    ; ESI = ptrB에 저장된 주소
;     mov al, [esi]    ; AL = 해당 주소의 값

; ╔══════════════════════════════════════════════════════════╗
; ║        SECTION 5: JMP 및 LOOP 명령어                    ║
; ╚══════════════════════════════════════════════════════════╝
;
; ─────────────────────────────────────────────────────────
; 5-1. 제어 전달의 두 종류
; ─────────────────────────────────────────────────────────
;   무조건 전달(Unconditional): 항상 점프 → JMP
;   조건부 전달(Conditional) : 플래그/ECX 조건에 따라 점프
;
; ─────────────────────────────────────────────────────────
; 5-2. JMP 명령어
; ─────────────────────────────────────────────────────────
;   형식: JMP 레이블
;   목적지 오프셋 → EIP에 로드
;   단독으로는 무한 루프 → 조건 분기(LOOP, Jcc)와 함께 사용
;
; ─────────────────────────────────────────────────────────
; 5-3. LOOP 명령어
; ─────────────────────────────────────────────────────────
;   ECX를 카운터로 사용하는 카운터 루프
;   형식: LOOP 레이블
;
;   실행 순서:
;     ① ECX -= 1
;     ② ECX != 0 이면 레이블로 점프 (계속)
;        ECX == 0 이면 다음 명령으로 진행 (종료)
;
;   제약: 목적지가 현재 위치에서 -128 ~ +127 바이트 이내
;
;   ★ 치명적 실수: ECX = 0으로 시작하면
;     LOOP 첫 실행에서 ECX = 0 - 1 = FFFFFFFFh (42억 회 반복!)
;
; ─────────────────────────────────────────────────────────
; 5-4. 루프 내에서 ECX 수정이 필요할 때
; ─────────────────────────────────────────────────────────
;   루프 시작에서 ECX를 변수에 저장, LOOP 직전에 복원
;   예)
;     mov ecx, 5
;   myLoop:
;     mov savedECX, ecx
;     ...ECX를 사용하는 코드...
;     mov ecx, savedECX   ; LOOP 직전 복원
;     loop myLoop
;
; ─────────────────────────────────────────────────────────
; 5-5. 중첩 루프 (Nested Loops)
; ─────────────────────────────────────────────────────────
;   외부 루프 카운터를 변수에 저장하여 내부 루프와 분리
;   예)
;     mov ecx, 3          ; 외부 루프 3회
;   outerLoop:
;     mov outerCnt, ecx   ; 외부 ECX 백업
;     mov ecx, 5          ; 내부 루프 5회
;   innerLoop:
;     ...
;     loop innerLoop
;     mov ecx, outerCnt   ; 외부 ECX 복원
;     loop outerLoop      ; 총 15회 실행
;
;   일반 규칙: 중첩 2단계 초과 시 내부 루프를 서브루틴으로 분리 권장
;
; ─────────────────────────────────────────────────────────
; 5-6. 배열 합산 표준 패턴 (7단계, 시험 단골!)
; ─────────────────────────────────────────────────────────
;   ① mov esi, OFFSET 배열이름    ; 배열 시작 주소 로드
;   ② mov ecx, LENGTHOF 배열이름  ; 루프 카운터 = 원소 수
;   ③ mov eax, 0                  ; 누적 합계 초기화
;   ④ (루프 레이블):              ; 루프 시작
;   ⑤ add eax, [esi]              ; 현재 원소 누적
;   ⑥ add esi, TYPE 배열이름      ; 다음 원소로 이동
;   ⑦ loop (루프 레이블)          ; 반복
;
; ─────────────────────────────────────────────────────────
; 5-7. 문자열 복사 패턴
; ─────────────────────────────────────────────────────────
;   MOV는 메모리-메모리 이동 불가 → AL을 경유
;     mov esi, 0
;     mov ecx, SIZEOF source
;   copyLoop:
;     mov al, source[esi]
;     mov target[esi], al
;     inc esi
;     loop copyLoop

; ╔══════════════════════════════════════════════════════════╗
; ║              SECTION 6: 흔한 실수 모음                   ║
; ╚══════════════════════════════════════════════════════════╝
;
; ✗ 실수 1: 메모리-메모리 직접 MOV
;   mov var2, var1      ; 오류! 메모리→메모리 불가
;   → 레지스터를 거쳐야 함: mov al, var1 / mov var2, al
;
; ✗ 실수 2: 크기 불일치 MOV
;   mov eax, bl         ; 오류! EAX(32bit) ≠ BL(8bit)
;   → movzx eax, bl  또는  movsx eax, bl 사용
;
; ✗ 실수 3: 간접 피연산자의 크기 미지정
;   inc [esi]           ; 오류! 크기 불명확
;   → inc DWORD PTR [esi]  (크기 명시 필수)
;
; ✗ 실수 4: LOOP 전 ECX = 0 초기화
;   mov ecx, 0
;   myLoop: ... loop myLoop  ; 42억 회 반복
;
; ✗ 실수 5: LENGTHOF와 SIZEOF 혼동
;   LENGTHOF arr = 원소 개수   (개수)
;   SIZEOF   arr = 전체 바이트 (바이트 크기)
;   루프 카운터엔 LENGTHOF, 메모리 크기엔 SIZEOF 사용
;
; ✗ 실수 6: 부호 있는 값에 MOVZX 사용
;   sVal SBYTE -16       ; = 0F0h
;   movzx eax, sVal      ; EAX = 000000F0h = +240 (오류! -16이 아님)
;   → movsx eax, sVal    ; EAX = FFFFFFF0h = -16 (올바름)
;
; ✗ 실수 7: OFFSET 없이 WriteString 에 문자열 주소 전달 (5장 연계)
;   mov edx, myStr       ; 오류: myStr의 첫 바이트 값이 EDX에 들어감
;   → mov edx, OFFSET myStr  (주소를 전달해야 함)

; ╔══════════════════════════════════════════════════════════╗
; ║           SECTION 7: 전체 퀴즈 정답 모음                 ║
; ╚══════════════════════════════════════════════════════════╝
;
; Q1: BL=F0h → movzx eax,bl / movsx eax,bl 후 EAX?
;   movzx → EAX = 000000F0h (+240)
;   movsx → EAX = FFFFFFF0h (-16)
;
; Q2: AL=127 → add al,1 후 플래그?
;   AL = 80h = -128, OF=1 (부호 오버플로), CF=0
;
; Q3: AL=0FFh → add al,1 후 플래그?
;   AL = 0, CF=1 (부호 없는 오버플로), ZF=1, OF=0
;
; Q4: arr DWORD 1,2,3,4,5 → TYPE/LENGTHOF/SIZEOF?
;   TYPE=4, LENGTHOF=5, SIZEOF=20
;
; Q5: wordArr 인덱스3 스케일 팩터 접근?
;   mov esi, 3  /  mov ax, [wordArr + esi*2]
